#Takes in an argument of a name and puts out a phrase with that name using string interpolation
def introduction(name)
  puts "Hi, my name is #{name}.\n"
end

#Take in two arguments, a name and a language and puts out a phrase using those arguments
def introduction_with_language(name,language)
  puts "Hi, my name is #{name} and I am learning to program in #{language}.\n"
end